        <p><b>Kompiuterinis žaidimas</b>, <b>vaizdo žaidimas</b> arba <b>videožaidimas</b> – žaidimas, kuris yra žaidžiamas kompiuteriu ar kitu specialiai tam pritaikytu įrenginiu (namų žaidimų konsole, delnine žaidimų konsole). Vaizdas gaunamas grįžtamuoju ryšiu, prie žaidimų kompiuterio prijungus monitorių arba televizorių. Nuo XXI a. 2-ojo deš. paplito vaizdo žaidimai, pritaikyti žaisti mobiliuosiuose įrenginiuose (išmaniuosiuose telefonuose, planšetėse).

